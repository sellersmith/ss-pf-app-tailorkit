type Action =
  | { type: 'SET_LOADING'; payload: { extracting: boolean } }
  | { type: 'INIT_DATA'; payload: { state: Partial<TemplateEditor> } }
  | { type: 'SET_TEMPLATE_GENERATED_DATA'; payload: { state: Partial<TemplateEditor> } }
  | { type: 'SET_NAME'; payload: { name: string } }
  | { type: 'SET_VIEW_PORT'; payload: { viewport: ViewPort } }
  | { type: 'SET_DIMENSION'; payload: { dimension: TemplateDimension } }
  | { type: 'SET_VIEW_PORT_AND_DIMENSION'; payload: { viewport: ViewPort; dimension: TemplateDimension } }
  | { type: 'SET_INTERACTIVE'; payload: { interactive: boolean } }
  | { type: 'SET_MEASUREMENT_UNIT'; payload: { fromUnit: MEASUREMENT_UNIT; toUnit: MEASUREMENT_UNIT } }
  | {
      type: 'SET_RESOLUTION'
      payload: { toResolution: TemplateDimension['resolution'] }
    }
  | { type: 'ADD_EXTRACTED_LAYER_IDS'; payload: { extractedLayerStores: TLayerStore[] } }
  | { type: 'DELETE_EXTRACTED_LAYER_IDS'; payload: { extractedLayerStores: TLayerStore[] } }
  | { type: 'SET_EXTRACTED_LAYER_IDS'; payload: { extractedLayerStores: TLayerStore[] } }
  | { type: 'SET_OPTION_SET_LISTS'; payload: { optionSetLists: OptionSet[] } }
  | { type: 'UPDATE_OPTION_SET_LISTS'; payload: { type: string; optionUpdatedLists: OptionSet[] } }
  | { type: 'SET_CLIPARTS'; payload: { extractedLayerStores?: any[]; clipartsAdded: TemplateEditor[] } }
  | { type: 'SET_CATEGORY'; payload: { category?: string } }
  | { type: 'SET_STAGE_REF'; payload: { stageRef: React.RefObject<Konva.Stage> | null } }
  | { type: 'SET_SIDEBAR_ACTIVE'; payload: { sidebarActive: boolean } }
  | { type: 'SET_ANCHOR_DRAGGING'; payload: { isAnchorDragging: boolean } }
  | {
      type: 'SET_PREVIEW_PRODUCT_IMAGE'
      payload: {
        previewProductImage:
          | TemplateEditor['previewProductImage']
          | Partial<NonNullable<TemplateEditor['previewProductImage']>>
        merge?: boolean
      }
    }
  | { type: 'SET_METADATA_SAVE_THUMBNAIL_WITH_PREVIEW'; payload: { saveThumbnailWithPreview: boolean } }
  | { type: 'RESET_STATE' }

