/**
 * Delete extracted layer stores
 * Handles cascade delete for CHARM_NODE layers (C2 fix)
 * @param extractedLayerStores - Layer stores to delete
 * @param skipTrace - Whether to skip trace
 */
function deleteExtractedLayerStores(extractedLayerStores: TLayerStore[], skipTrace?: boolean) {
  const layersToDelete: TLayerStore[] = [...extractedLayerStores]
  const charmInstanceIdsToUnregister: string[] = []

  // Check for CHARM_NODE layers and cascade delete their CHARM children
  for (const store of extractedLayerStores) {
    const state = store.getState()

    if (state.type === ELayerType.CHARM_NODE) {
      const settings = state.settings as CharmNodeSettings | undefined
      const linkedProducts = settings?.linkedProducts || []

      // Collect all instanceIds from this CHARM_NODE
      for (const product of linkedProducts) {
        for (const transform of product.transforms || []) {
          charmInstanceIdsToUnregister.push(transform.instanceId)

          // Find the CHARM layer for this instance
          const charmLayer = getCharmLayerByInstanceId(transform.instanceId)
          if (charmLayer && !layersToDelete.includes(charmLayer)) {
            layersToDelete.push(charmLayer)
          }
        }
      }
    }

    // If deleting a CHARM layer, unregister it from the index
    if (state.type === ELayerType.CHARM) {
      const instanceId = (state.settings as any)?.productRef?.instanceId
      if (instanceId) {
        charmInstanceIdsToUnregister.push(instanceId)
      }
    }
  }

  // Mark all CHARM children as deleted (for undo/redo support)
  for (const store of layersToDelete) {
    const state = store.getState()
    if (state.type === ELayerType.CHARM) {
      store.dispatch({
        type: 'UPDATE_LAYER',
        payload: { state: { isDeletedOnEditor: true } },
        skipTrace: true,
      })
    }
  }

  // Unregister charm layers from index
  for (const instanceId of charmInstanceIdsToUnregister) {
    unregisterCharmLayer(instanceId)
  }

  TemplateEditorStore.dispatch({
    type: 'DELETE_EXTRACTED_LAYER_IDS',
    payload: { extractedLayerStores: layersToDelete },
    skipTrace,
  })
}

export function getExtractedLayerStores() {
  return TemplateEditorStore.getState().extractedLayerStores
}

/** This function return all layers include layers in multi-layout */
export function getExtractedCompositeLayerStores() {
  const extractedLayerStores = getExtractedLayerStores()

  const compositeLayerStores: TLayerStore[] = []

  extractedLayerStores.forEach(layerStore => {
    const layerState = layerStore.getState()

    if (layerState.isDeletedOnEditor) return

    compositeLayerStores.push(layerStore)

    const type = layerState.type
    if (type === 'multi-layout') {
      const optionSet = layerState.optionSet?.find(ot => ot.type === EOptionSet['MULTI_LAYOUT_OPTION'])

      if (!optionSet) return

      const layerStores = (optionSet.data?.['multi_layout']?.layouts || [])
        .map(layout => layout.layerIds.map(layerId => getLayerStoreById(layerId)))
        .flat()

      compositeLayerStores.push(...layerStores)
    }
  })

  return compositeLayerStores
}

function setExtractedLayerStores(extractedLayerStores: TLayerStore[], skipTrace?: boolean) {
  TemplateEditorStore.dispatch({ type: 'SET_EXTRACTED_LAYER_IDS', payload: { extractedLayerStores }, skipTrace })
}

function resetState(skipTrace?: boolean) {
  // Clear charm layer index to prevent stale entries when switching templates
  clearCharmLayerIndex()
  TemplateEditorStore.dispatch({ type: 'RESET_STATE', skipTrace })
}

function setOptionSetLists(optionSetLists: OptionSet[], skipTrace?: boolean) {
  TemplateEditorStore.dispatch({ type: 'SET_OPTION_SET_LISTS', payload: { optionSetLists }, skipTrace })
}

function resetClipartUsages(clipartsAdded: TemplateEditor[] = [], skipTrace?: boolean) {
  TemplateEditorStore.dispatch({
    type: 'SET_CLIPARTS',
    payload: { clipartsAdded },
    skipTrace,
  })
}

