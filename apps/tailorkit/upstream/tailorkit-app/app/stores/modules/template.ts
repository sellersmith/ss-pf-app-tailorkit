// Define a store for layer visibility
export const LayerVisibilityStore = createStore(LayerVisibilityStoreReducer, { layerVisibility: {} })

function LayerVisibilityStoreReducer(state: any, action: any) {
  const payload = action.payload

  switch (action.type) {
    case 'SET_LAYER_VISIBILITY':
      return {
        ...state,
        layerVisibility: {
          ...state.layerVisibility,
          [payload.layerId]: { ...(state.layerVisibility?.[payload.layerId] || {}), ...payload.visible },
        },
      }

    case 'RESET_LAYER_VISIBILITY':
      return {
        ...state,
        layerVisibility: null,
      }

    default:
      return state
  }
}
