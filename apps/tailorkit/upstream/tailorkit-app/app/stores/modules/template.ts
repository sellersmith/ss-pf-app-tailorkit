const templateEditorReducer: Reducer<TemplateEditor, Action> = (state, action) => {
  switch (action.type) {
    case 'SET_LOADING': {
      const payload = action.payload
      return {
        ...state,
        extracting: payload.extracting,
      }
    }

    case 'SET_TEMPLATE_GENERATED_DATA':
    case 'INIT_DATA': {
      const payload = action.payload

      return {
        ...state,
        ...(payload.state || payload),
      }
    }

    case 'SET_NAME': {
      const payload = action.payload

      const name = payload.name
      return {
        ...state,
        // Only input words, numbers and space
        name: name,
      }
    }

    case 'SET_VIEW_PORT': {
      const payload = action.payload

      const viewport = payload.viewport
      return {
        ...state,
        viewport,
      }
    }

    case 'SET_DIMENSION': {
      const payload = action.payload

      const { width, height, measurementUnit, resolution } = payload.dimension

      const _dimension = { width, height, measurementUnit, resolution }

      const viewport = revaluateCanvasViewPort(_dimension)

      return {
        ...state,
        dimension: _dimension,
        viewport,
      }
    }

    case 'SET_VIEW_PORT_AND_DIMENSION': {
      const payload = action.payload

      const { viewport, dimension } = payload

      return {
        ...state,
        dimension,
        viewport,
      }
    }

    case 'SET_INTERACTIVE': {
      const { interactive } = action.payload

      return {
        ...state,
        interactive,
      }
    }

    case 'SET_MEASUREMENT_UNIT': {
      const payload = action.payload

      const { fromUnit, toUnit } = payload
      const dimension = state.dimension

      if (!dimension) {
        return {
          ...state,
        }
      }

      const { width, height, resolution } = dimension

      const _width = lengthUnitToLengthUnit(fromUnit, toUnit, width, resolution)
      const _height = lengthUnitToLengthUnit(fromUnit, toUnit, height, resolution)

      const _dimension = { width: _width, height: _height, measurementUnit: toUnit, resolution }

      const viewport = revaluateCanvasViewPort(_dimension)

      return {
        ...state,
        dimension: _dimension,
        viewport,
      }
    }

    case 'SET_RESOLUTION': {
      const payload = action.payload

      const { toResolution } = payload
      const dimension = state.dimension

      if (!dimension) {
        return {
          ...state,
        }
      }

      const { width, height, /*resolution,*/ measurementUnit } = dimension

      /* Re-evaluate the width & height
      const _width = resizeDimensionFromResolutionToResolution(
        resolution,
        toResolution,
        width,
        dimension.measurementUnit
      )

      const _height = resizeDimensionFromResolutionToResolution(
        resolution,
        toResolution,
        height,
        dimension.measurementUnit
      )*/

      const _dimension = { width /*: _width*/, height /*: _height*/, resolution: toResolution, measurementUnit }

      const viewport = revaluateCanvasViewPort(_dimension)

      return {
        ...state,
        dimension: _dimension,
        viewport,
      }
    }

    case 'ADD_EXTRACTED_LAYER_IDS': {
      const payload = action.payload

      return {
        ...state,
        extractedLayerStores: [...payload.extractedLayerStores, ...state.extractedLayerStores],
      }
    }

    case 'DELETE_EXTRACTED_LAYER_IDS': {
      const payload = action.payload

      const deletedExtractedLayerStores = payload.extractedLayerStores

      return {
        ...state,
        // Exclude the layer stores out of the state extractedLayerStores
        extractedLayerStores: state.extractedLayerStores.filter(
          layerStore =>
            !deletedExtractedLayerStores
              .map(deletedLayerStore => deletedLayerStore.getState()._id)
              .includes(layerStore.getState()._id)
        ),
      }
    }

    case 'SET_CLIPARTS': {
      const payload = action.payload

      return {
        ...state,
        ...payload,

        /** @deprecated Previous is ADD_CLIPARTS */
        // extractedLayerStores: [...extractedLayerStores, ...state.extractedLayerStores],
      }
    }

    case 'SET_EXTRACTED_LAYER_IDS': {
      const payload = action.payload

      return {
        ...state,
        extractedLayerStores: payload.extractedLayerStores,
      }
    }

    case 'SET_STAGE_REF': {
      const payload = action.payload

      return {
        ...state,
        stageRef: payload.stageRef,
      }
    }

    case 'SET_SIDEBAR_ACTIVE': {
      const payload = action.payload

      return {
        ...state,
        sidebarActive: payload.sidebarActive,
      }
    }

    case 'SET_PREVIEW_PRODUCT_IMAGE': {
      const { previewProductImage, merge } = action.payload
      // If merge requested and both current and incoming are objects, merge; else replace (including null to clear)
      const nextPreview
        = merge && state.previewProductImage && previewProductImage && typeof previewProductImage === 'object'
          ? { ...state.previewProductImage, ...previewProductImage }
          : (previewProductImage as TemplateEditor['previewProductImage'])

      return {
        ...state,
        previewProductImage: nextPreview ?? null,
      }
    }

    case 'RESET_STATE':
      return DEFAULT_TEMPLATE_EDITOR_STORE

    case 'SET_OPTION_SET_LISTS':
      const payload = action.payload
      const { TEXT_OPTION, IMAGE_OPTION, COLOR_OPTION, FONT_OPTION, MASK_OPTION } = EOptionSet
      const allOptionSetListsDefault: any = {
        [TEXT_OPTION]: [],
        [IMAGE_OPTION]: [],
        [COLOR_OPTION]: [],
        [FONT_OPTION]: [],
        [MASK_OPTION]: [],
      }

      const allOptionSetList = payload.optionSetLists.reduce((acc, option) => {
        if (acc[option.type]) {
          acc[option.type].push(option)
        }
        return acc
      }, allOptionSetListsDefault)

      return {
        ...state,
        allOptionSetList,
      }

    case 'UPDATE_OPTION_SET_LISTS': {
      const { type, optionUpdatedLists } = action.payload
      const allOptionSetList = state.allOptionSetList || {}

      return {
        ...state,
        allOptionSetList: {
          ...allOptionSetList,
          [type]: optionUpdatedLists,
        },
      }
    }

    case 'SET_CATEGORY': {
      const { category } = action.payload
      return {
        ...state,
        category,
      }
    }

    case 'SET_ANCHOR_DRAGGING': {
      const { isAnchorDragging } = action.payload
      return {
        ...state,
        isAnchorDragging,
      }
    }

    case 'SET_METADATA_SAVE_THUMBNAIL_WITH_PREVIEW': {
      const { saveThumbnailWithPreview } = action.payload
      return {
        ...state,
        metadata: {
          ...state.metadata,
          saveThumbnailWithPreview,
        },
      }
    }

    default:
      return state
  }
}

function proxyTemplateEditorReducer(state: TemplateEditor, action: Action & Trace) {
  const updatedState = templateEditorReducer(state, action)

  const skipTrace = action.skipTrace

  if (!skipTrace) {
    const step: Step = {
      type: action.type,
      fromData: state,
      toData: updatedState,
      callback: (target: any, props: string | symbol, value: any) => stepCallback(target, props, value, getSaveBarId()),
    }

    addSteps(step)
  }

  return updatedState
}

