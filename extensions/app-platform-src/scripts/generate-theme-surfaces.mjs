// App-platform theme build workspace owns generated Shopify extension output for internal apps.
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import { discoverThemeSurfaceApps, selectThemeSurfaceApps } from './theme-surface-loader.mjs'

const scriptRoot = path.dirname(fileURLToPath(import.meta.url))
const extensionRoot = path.resolve(scriptRoot, '..')
const repoRoot = path.resolve(extensionRoot, '../..')
const themeHelperRoot = path.join(repoRoot, 'extensions/pagefly-theme-helper')

// Liquid files are generated into PageFly's existing Shopify extension shell. Do not add OneTick
// source directly under pagefly-theme-helper; app-owned source remains under apps/*/theme-extension.
function liquidGeneratedHeader(source) {
  return `{% comment %}\n  GENERATED BY app-platform. Source: ${source}\n  Do not edit this file directly.\n{% endcomment %}\n\n`
}

function writeGeneratedLiquid(sourceRoot, source, outputDir, generatedName) {
  const sourcePath = path.join(sourceRoot, source)
  const outputPath = path.join(outputDir, generatedName)
  const body = fs.readFileSync(sourcePath, 'utf8')

  fs.mkdirSync(path.dirname(outputPath), { recursive: true })
  fs.writeFileSync(outputPath, `${liquidGeneratedHeader(path.relative(repoRoot, sourcePath))}${body}`)
}

function assertGeneratedAssets(themeSurfaces) {
  for (const asset of themeSurfaces.assets || []) {
    const outputPath = path.join(themeHelperRoot, 'assets', asset.generatedName)
    if (!fs.existsSync(outputPath)) {
      throw new Error(`Missing generated app-platform theme asset: ${path.relative(repoRoot, outputPath)}`)
    }
  }
}

function deepMergeLocale(target, source) {
  for (const [key, value] of Object.entries(source)) {
    if (value && typeof value === 'object' && !Array.isArray(value)) {
      target[key] = deepMergeLocale(target[key] && typeof target[key] === 'object' ? target[key] : {}, value)
    } else {
      target[key] = value
    }
  }
  return target
}

// Locale JSON is shared across all apps in one extension, so app-owned groups are deep-merged into
// the deploy-shell locale file. JSON cannot carry a generated-by comment, so provenance lives here.
function mergeGeneratedLocale(sourceRoot, source, generatedName) {
  const sourcePath = path.join(sourceRoot, source)
  const outputPath = path.join(themeHelperRoot, 'locales', generatedName)
  const appLocale = JSON.parse(fs.readFileSync(sourcePath, 'utf8'))
  const existing = fs.existsSync(outputPath) ? JSON.parse(fs.readFileSync(outputPath, 'utf8')) : {}

  fs.mkdirSync(path.dirname(outputPath), { recursive: true })
  fs.writeFileSync(outputPath, `${JSON.stringify(deepMergeLocale(existing, appLocale), null, 2)}\n`)
}

function assertNoQuarantinedAssets(app) {
  const quarantinedAssets = (app.themeSurfaces.assets || []).filter(
    asset => asset.sourceStatus === 'quarantined-rewrite'
  )
  if (!quarantinedAssets.length) return

  const sources = quarantinedAssets
    .map(asset => path.relative(repoRoot, path.resolve(repoRoot, app.sourceRoot, asset.source)).split(path.sep).join('/'))
    .join(', ')

  throw new Error(`Cannot generate theme surfaces for quarantined theme asset source: ${sources}`)
}

function generateThemeSurface(app) {
  const sourceRoot = path.join(repoRoot, app.sourceRoot)
  assertNoQuarantinedAssets(app)

  for (const file of app.themeSurfaces.appEmbeds || []) {
    writeGeneratedLiquid(sourceRoot, file.source, path.join(themeHelperRoot, 'blocks'), file.generatedName)
  }
  for (const file of app.themeSurfaces.appBlocks || []) {
    writeGeneratedLiquid(sourceRoot, file.source, path.join(themeHelperRoot, 'blocks'), file.generatedName)
  }
  for (const file of app.themeSurfaces.snippets || []) {
    writeGeneratedLiquid(sourceRoot, file.source, path.join(themeHelperRoot, 'snippets'), file.generatedName)
  }
  for (const file of app.themeSurfaces.locales || []) {
    mergeGeneratedLocale(sourceRoot, file.source, file.generatedName)
  }
  assertGeneratedAssets(app.themeSurfaces)
}

function parseAppId(args) {
  const appFlagIndex = args.findIndex(arg => arg === '--app')
  if (appFlagIndex >= 0) return args[appFlagIndex + 1]
  const appEquals = args.find(arg => arg.startsWith('--app='))
  return appEquals?.slice('--app='.length)
}

export function generateThemeSurfaces(options = {}) {
  const apps = selectThemeSurfaceApps(discoverThemeSurfaceApps(repoRoot), options.appId, {
    includeOnDemand: options.includeOnDemand,
  })
  apps.forEach(generateThemeSurface)
}

if (process.argv[1] && fileURLToPath(import.meta.url) === path.resolve(process.argv[1])) {
  const args = process.argv.slice(2)
  generateThemeSurfaces({ appId: parseAppId(args), includeOnDemand: args.includes('--all') })
}
