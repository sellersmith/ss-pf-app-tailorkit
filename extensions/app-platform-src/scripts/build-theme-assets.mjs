import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import { build } from 'vite'
import { build as esbuild } from 'esbuild'
import { discoverThemeSurfaceApps, selectThemeSurfaceApps } from './theme-surface-loader.mjs'

const scriptRoot = path.dirname(fileURLToPath(import.meta.url))
const extensionRoot = path.resolve(scriptRoot, '..')
const repoRoot = path.resolve(extensionRoot, '../..')
const themeHelperAssetRoot = path.join(repoRoot, 'extensions/pagefly-theme-helper/assets')

function appGlobalName(appId) {
  return `${appId.replace(/(^|-)([a-z0-9])/g, (_, _dash, letter) => letter.toUpperCase())}StorefrontRuntime`
}

function generatedHeader(source) {
  return `/* GENERATED BY app-platform. Source: ${source}. Do not edit directly. */`
}

function parseAppId(args) {
  const appFlagIndex = args.findIndex(arg => arg === '--app')
  if (appFlagIndex >= 0) return args[appFlagIndex + 1]
  const appEquals = args.find(arg => arg.startsWith('--app='))
  return appEquals?.slice('--app='.length)
}

export function parseBuildOptions(args = []) {
  return {
    appId: parseAppId(args),
    includeOnDemand: args.includes('--all'),
    watch: args.includes('--watch'),
  }
}

function appAssets(apps) {
  return apps.flatMap(app =>
    (app.themeSurfaces.assets || []).map(asset => ({
      appId: app.themeSurfaces.appId,
      entry: path.resolve(repoRoot, app.sourceRoot, asset.source),
      generatedName: asset.generatedName,
      kind: asset.kind,
      sourceStatus: asset.sourceStatus,
    }))
  )
}

function relativeSource(sourcePath) {
  return path.relative(repoRoot, sourcePath).split(path.sep).join('/')
}

export function assertThemeAssetsNotQuarantined(apps) {
  const quarantinedAssets = appAssets(apps).filter(asset => asset.sourceStatus === 'quarantined-rewrite')

  if (!quarantinedAssets.length) return

  const sources = quarantinedAssets.map(asset => relativeSource(asset.entry)).join(', ')

  throw new Error(`Cannot materialize quarantined theme asset source: ${sources}`)
}

// Bundle the stylesheet so the source `@import` graph (TailorKit's tailorkit.css pulls in ~24
// component stylesheets) is flattened into one file. Shopify's extension asset copy does not resolve
// nested @import, so the materialized asset MUST be self-contained. esbuild inlines the import graph.
async function copyStylesheetAsset(asset) {
  const source = path.relative(repoRoot, asset.entry)
  const outputPath = path.join(themeHelperAssetRoot, asset.generatedName)

  const result = await esbuild({
    entryPoints: [asset.entry],
    bundle: true,
    write: false,
    minify: false,
    loader: { '.css': 'css', '.png': 'dataurl', '.jpg': 'dataurl', '.svg': 'dataurl', '.woff': 'dataurl', '.woff2': 'dataurl' },
    logLevel: 'silent',
  })
  const css = result.outputFiles[0].text

  fs.mkdirSync(path.dirname(outputPath), { recursive: true })
  fs.writeFileSync(outputPath, `${generatedHeader(source)}\n${css}`)
}

async function buildJavascriptAsset(asset, watch) {
  const source = path.relative(repoRoot, asset.entry)
  await build({
    configFile: false,
    root: extensionRoot,
    // TailorKit storefront components are Preact (each tsx declares `@jsxImportSource preact`). Without
    // this, Vite's default esbuild compiles JSX to `React.createElement`, and React isn't bundled — the
    // storefront throws `React is not defined` when a Preact modal mounts (e.g. the image editor).
    // Routing JSX to Preact's automatic runtime makes the pragma effective for the whole bundle.
    esbuild: {
      jsx: 'automatic',
      jsxImportSource: 'preact',
    },
    // Alias React to preact/compat so any transitive `react`/`react-dom` import resolves to Preact
    // instead of a missing React global (parity with the upstream TailorKit vite.features.config).
    resolve: {
      alias: {
        react: 'preact/compat',
        'react-dom': 'preact/compat',
        'react/jsx-runtime': 'preact/jsx-runtime',
      },
    },
    build: {
      assetsDir: '',
      emptyOutDir: false,
      minify: false,
      sourcemap: false,
      target: 'es2019',
      outDir: '../pagefly-theme-helper/assets',
      watch: watch ? {} : null,
      lib: {
        entry: asset.entry,
        fileName: () => asset.generatedName,
        formats: ['iife'],
        name: appGlobalName(asset.appId),
      },
      rollupOptions: {
        output: {
          inlineDynamicImports: true,
        },
        plugins: [
          {
            name: 'app-platform-generated-header',
            generateBundle(_outputOptions, bundle) {
              const file = bundle[asset.generatedName]
              if (file && 'code' in file) {
                file.code = `${generatedHeader(source)}\n${file.code}`
              }
            },
          },
        ],
      },
    },
  })
}

export async function buildThemeAssets(options = {}) {
  const apps = selectThemeSurfaceApps(discoverThemeSurfaceApps(repoRoot), options.appId, {
    includeOnDemand: options.includeOnDemand,
  })
  assertThemeAssetsNotQuarantined(apps)
  const assets = appAssets(apps)

  for (const asset of assets) {
    if (asset.kind === 'stylesheet') {
      await copyStylesheetAsset(asset)
      continue
    }
    await buildJavascriptAsset(asset, Boolean(options.watch))
  }
}
